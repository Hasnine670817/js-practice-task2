/*
Problem 7: Print Each Character

Write a program that prints each character of a string on a new line.
Hint: Use a for loop to go through the string.

Sample Input: "Code"
Sample Output:
C
o
d
e
*/

// Solution: 

const text = "Code";

for (let i = 0; i < text.length; i++) {
    console.log(text[i]);
}

/* Output: 
C
o
d
e
*/